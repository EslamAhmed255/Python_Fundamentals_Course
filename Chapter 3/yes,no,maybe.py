print("Welcome to yes,no,maybe project")

put = input("Please write yes or no or maybe \n")

if put == "yes":
    print("Good, you typed YES")

elif put == "no":
    print("Good, you typed NO")

elif put == "maybe":
    print("Good, you typed MAYBE")
    
else:
    print("Oops, you didn't write YES or NO or MAYBE, please try again")